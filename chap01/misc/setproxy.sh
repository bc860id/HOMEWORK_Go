# プロキシ環境で演習でプロキシ外にアクセスするための設定を行う。
# このファイルを読み込んだプロセスの環境変数として反映させるため、
# 以下の様にドットコマンドを用いること。
#	. setproxyenv
#
# 上記入力は特殊なので以下の様に.profileでaliasを定義するとよい。
#	alias setproxy='. setproxyenv';
#
# 上記でaliasを定義した場合はプロキシ設定を以下の入力で行える。
#	setproxy
#
# このファイルをシェルスクリプトにして、
# 親プロセスへの反映ではなくスクリプトで環境変数を定義した後
# 続けてbashを子プロセスとして起動する方法もあるがやめておく。
#---------------------------------------------------------------------------

#---------------------------------------------------------------------------
# 以下のuuuをプロキシのユーザ名、pppをプロキシのパスワードに置き換える。
#---------------------------------------------------------------------------
export http_proxy=http://uuu:ppp@proxy.ricoh.co.jp:8080/
export https_proxy=$http_proxy


